        line = line.strip()
        
        if not line:
            if in_table:
                # End table
                html += "        </table>\n"
                in_table = False
            elif in_list:
                # End list
                html += f"        </{list_type}>\n"
                in_list = False
            else:
                html += "        <br>\n"
            continue
        
        # Handle different line types
        if line.startswith('# '):
            # Main title
            title = escape_html(line[2:])
            html += f"        <h1>{title}</h1>\n"
        elif line.startswith('## '):
            # Section heading
            title = escape_html(line[3:])
            html += f"        <h2>{title}</h2>\n"
        elif line.startswith('### '):
            # Subsection heading
            title = escape_html(line[4:])
            html += f"        <h3>{title}</h3>\n"
        elif line.startswith('#### '):
            # Sub-subsection heading
            title = escape_html(line[5:])
            html += f"        <h4>{title}</h4>\n"
        elif line.startswith('|') and '|' in line[1:]:
            # Table row
            if not in_table:
                # Start table
                html += "        <table>\n"
                in_table = True
            
            # Process table row
            cells = [cell.strip() for cell in line.split('|')[1:-1]]
            escaped_cells = [escape_html(cell) for cell in cells]
            
            # Check if this is a header row (contains dashes or is the first row)
            if any('---' in cell or '--' in cell for cell in cells) or not any('---' in prev_line for prev_line in lines if '|' in prev_line):
                html += "            <tr>\n"
                for cell in escaped_cells:
                    html += f"                <th>{cell}</th>\n"
                html += "            </tr>\n"
            else:
                html += "            <tr>\n"
                for cell in escaped_cells:
                    html += f"                <td>{cell}</td>\n"
                html += "            </tr>\n"
        elif line.startswith('- ') or line.startswith('* '):
            # Bullet point
            if not in_list:
                html += "        <ul>\n"
                in_list = True
                list_type = 'ul'
            text = escape_html(line[2:])
            html += f"            <li>{text}</li>\n"
        elif line.startswith('1. ') or line.startswith('2. ') or line.startswith('3. ') or line.startswith('4. ') or line.startswith('5. '):
            # Numbered list
            if not in_list:
                html += "        <ol>\n"
                in_list = True
                list_type = 'ol'
            text = escape_html(line[3:])
            html += f"            <li>{text}</li>\n"
        elif line.startswith('**') and line.endswith('**'):
            # Bold text
            text = escape_html(line[2:-2])
            html += f"        <p><strong>{text}</strong></p>\n"
        elif line.startswith('```'):
            # Code block
            if line == '```':
                html += "        <div class=\"code-block\">\n"
            else:
                html += f"        <div class=\"code-block\">\n            {escape_html(line[3:])}\n"
